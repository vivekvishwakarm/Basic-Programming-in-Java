public class PrintZ {
    public static void main(String[] args) {
        // Print Z pattern
        /*
        *****
           *
          *
         *
        *****
         */
        // Multiple print Use
        System.out.println("*****");
        System.out.println("   *");
        System.out.println("  *");
        System.out.println(" *");
        System.out.println("*****");

        System.out.println();
        //Only one print use
        System.out.println("*****\n   *\n  *\n *\n*****");
    }
}
