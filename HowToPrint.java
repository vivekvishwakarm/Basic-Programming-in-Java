public class HowToPrint {
    public static void main(String[] args) {
        // Here we will learn different type of method for printing

        // Knowing different type of printing
        System.out.print("Hello!");
        System.out.print("Sir\n");// go to next line

        System.out.println("Hello!");// go to next line
        System.out.print("Sir");

        /*
        *
        **
        ***
        ****
        *****
         */
        System.out.println();
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println("******");

        System.out.println();
        // Here we will print in one line pattern
        System.out.println("*\n**\n***\n****\n*****");
    }
}
